na = 20
nb = 60
operatore = '+'

if operatore == '+':
    hasil_resya = na + nb
elif operatore == '-':
    hasil_resya = na - nb
elif operatore == '*':
    hasil_resya = na * nb
elif operatore == '/':
    hasil_resya = na / nb
else:
    print('Data yang Anda masukkan salah')

print("Hasil:", hasil_resya)
